    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        //Admin Search Button
        try{
            stmt=conn.createStatement();
            int adminId=Integer.parseInt(stdid.getText());

            String sql ="select * from student where id='"+adminId+"'";
            rs=stmt.executeQuery(sql);
            if(rs.next())
            {
                stdname.setText(rs.getString("stdname"));
                father.setText(rs.getString("stdfathername"));
                surname.setText(rs.getString("stdsurname"));
                phoneno.setText(rs.getString("stdphone"));
                stdclass.setText(rs.getString("class"));
                blood.setText(rs.getString("stdblood"));
                city.setText(rs.getString("city"));

            }else
            {
                JOptionPane.showMessageDialog(null,"Record Not Found.");
            }
        }catch(HeadlessException | NumberFormatException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchActionPerformed

