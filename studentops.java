    private void adminsubmitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminsubmitbtnActionPerformed
        // TODO add your handling code here:
        //update Btn

        try{
            stmt=conn.createStatement();
            int adminId=Integer.parseInt(stdid.getText());

            String stdName =stdname.getText();
            String stdFather=father.getText();
            String surName=surname.getText();
            String phoneNo=phoneno.getText();
            String stdClass=stdclass.getText();
            String stdBlood=blood.getText();
            String stdCity=city.getText();
            String sql;
            sql = "UPDATE student SET stdname='"+stdName+"',stdfathername='"+stdFather+"',stdsurname ='"+surName+"',stdphone='"+phoneNo+"',class='"+stdClass+"',stdBlood='"+stdBlood+"',city='"+stdCity+"' WHERE id='"+adminId+"'";

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Data Updated Successfully.");
        }catch(HeadlessException | NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_adminsubmitbtnActionPerformed

